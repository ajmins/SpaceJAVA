package udemyPractices.Composition;

public class CompositionResolution {

	private int width;
	private int height;
	
	public CompositionResolution(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
	
}
