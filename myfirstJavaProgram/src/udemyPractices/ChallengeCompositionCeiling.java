package udemyPractices;

public class ChallengeCompositionCeiling {

	private int height;
	private int paintcolour;
	
	public ChallengeCompositionCeiling(int height, int paintcolour) {
		this.height = height;
		this.paintcolour = paintcolour;
	}

	public int getHeight() {
		return height;
	}

	public int getPaintcolour() {
		return paintcolour;
	}
	
	
	
}
